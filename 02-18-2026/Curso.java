public class Curso {

    int id;
    String nombreCurso;
    String profesor;
    int cantidadEstudiantes;

    public Curso(int id, String nombreCurso, String profesor, int cantidadEstudiantes) {
        this.id = id;
        this.nombreCurso = nombreCurso;
        this.profesor = profesor;
        this.cantidadEstudiantes = cantidadEstudiantes;
    }
}
